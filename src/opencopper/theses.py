"""Thesis ledger: every call the system makes, marked to market daily.

Two populations share one scorecard:

- **Registered theses** (`data/theses.yaml`) — the machine-readable twin of
  PREDICTIONS.md. Markable metrics grade themselves off keyless series;
  `external` ones stay open until a hand-added `resolution:` block cites a
  source. Past-deadline externals show as NEEDS-RES, loudly.
- **Auto theses** (`data/theses-auto.json`) — generated by the news pipeline:
  each distinct rule-matched supply event becomes "price rises ≥X% vs entry
  within H months", with the entry price snapshotted at creation. These test
  the news→simulation loop's actual predictive value, which is the question
  a quant asks of any signal: not "is the mechanism plausible" but "what
  happened next, every time it fired".

Statuses: OPEN (deadline ahead), HIT, MISS, NEEDS-RES (external, past
deadline, unresolved). Analytics: hit rate over resolved, Brier score where
the model attached a probability, and the open auto-theses' current
paper move. Scoring is the product; nothing here sizes or recommends.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass
from datetime import date, datetime, timezone
from pathlib import Path

import yaml

REGISTRY_PATH = Path("data/theses.yaml")
AUTO_PATH = Path("data/theses-auto.json")


@dataclass
class Marked:
    id: str
    created: str
    commodity: str
    claim: str
    kind: str          # registered | auto
    metric: str
    status: str        # open | hit | miss | needs-res
    reading: str       # human-readable current state
    deadline: str
    resolved_on: str | None = None
    prob: float | None = None
    move_pct: float | None = None  # current move vs entry (auto theses)


def _months(commodity: str) -> list[tuple[str, float]]:
    from .history import load_price_history

    h = load_price_history(commodity)
    return h.months if h else []


def _add_months(d: date, n: int) -> date:
    y, m = d.year + (d.month - 1 + n) // 12, (d.month - 1 + n) % 12 + 1
    return date(y, m, 1)


def _today() -> date:
    return datetime.now(timezone.utc).date()


def load_registry(path: Path = REGISTRY_PATH) -> list[dict]:
    if not path.exists():
        return []
    return yaml.safe_load(path.read_text())["theses"]


def load_auto(path: Path = AUTO_PATH) -> list[dict]:
    if not path.exists():
        return []
    return json.loads(path.read_text())


def mark_registered(t: dict, today: date | None = None) -> Marked:
    today = today or _today()
    deadline = date.fromisoformat(t["resolves_by"])
    base = dict(id=t["id"], created=t["created"], commodity=t["commodity"],
                claim=t["claim"], kind="registered", metric=t["metric"],
                deadline=t["resolves_by"], prob=t.get("prob"))

    if t.get("resolution"):  # manual resolution wins for any metric
        r = t["resolution"]
        return Marked(**base, status=r["outcome"], resolved_on=r["date"],
                      reading=r.get("note", r["outcome"]))

    if t["metric"] == "annual_avg_band":
        rows = [v for d, v in _months(t["commodity"]) if d.startswith(str(t["year"]))]
        if not rows:
            return Marked(**base, status="open", reading="no readings yet")
        avg = sum(rows) / len(rows)
        inside = t["lo"] <= avg <= t["hi"]
        if len(rows) >= 12:  # the year has fully printed
            return Marked(**base, status="hit" if inside else "miss",
                          resolved_on=f"{t['year']}-12-31",
                          reading=f"{t['year']} avg {avg:,.0f} vs [{t['lo']:,}-{t['hi']:,}]")
        return Marked(**base, status="open",
                      reading=f"YTD avg {avg:,.0f} ({len(rows)} mo) vs [{t['lo']:,}-{t['hi']:,}]"
                              f"{' — inside' if inside else ' — OUTSIDE'}")

    # external: trackable only by hand
    if today > deadline:
        return Marked(**base, status="needs-res",
                      reading="past deadline — add a resolution: block with a source")
    return Marked(**base, status="open", reading="awaiting external resolution")


def mark_auto(t: dict, today: date | None = None) -> Marked:
    today = today or _today()
    created = date.fromisoformat(t["created"])
    deadline = _add_months(created, t["horizon_months"])
    base = dict(id=t["id"], created=t["created"], commodity=t["commodity"],
                claim=t["claim"], kind="auto", metric="price_change",
                deadline=deadline.isoformat())

    months = _months(t["commodity"])
    after = [(d, v) for d, v in months if d > t["entry_date"]]
    entry = t["entry_price"]
    threshold = entry * (1 + t["min_move_pct"] / 100)

    # first monthly print at/over the threshold resolves the thesis
    for d, v in after:
        if date.fromisoformat(d) > deadline:
            break
        if v >= threshold:
            return Marked(**base, status="hit", resolved_on=d,
                          move_pct=round(100 * (v / entry - 1), 1),
                          reading=f"{v:,.0f} ≥ {threshold:,.0f} on {d[:7]}")
    current = after[-1] if after else (t["entry_date"], entry)
    move = round(100 * (current[1] / entry - 1), 1)
    if today > deadline:
        return Marked(**base, status="miss", resolved_on=deadline.isoformat(),
                      move_pct=move,
                      reading=f"never printed ≥ {threshold:,.0f}; ended {move:+.1f}%")
    return Marked(**base, status="open", move_pct=move,
                  reading=f"now {current[1]:,.0f} ({move:+.1f}% vs entry {entry:,.0f})")


def mark_all(today: date | None = None) -> list[Marked]:
    out = [mark_registered(t, today) for t in load_registry()]
    out += [mark_auto(t, today) for t in load_auto()]
    return out


def analytics(marked: list[Marked]) -> dict:
    by = lambda s: [m for m in marked if m.status == s]
    resolved = by("hit") + by("miss")
    briers = [(m.prob - (1.0 if m.status == "hit" else 0.0)) ** 2
              for m in resolved if m.prob is not None]
    open_auto = [m for m in marked if m.kind == "auto" and m.status == "open"
                 and m.move_pct is not None]
    return {
        "n": len(marked),
        "open": len(by("open")),
        "hit": len(by("hit")),
        "miss": len(by("miss")),
        "needs_res": len(by("needs-res")),
        "hit_rate": round(len(by("hit")) / len(resolved), 3) if resolved else None,
        "brier": round(sum(briers) / len(briers), 3) if briers else None,
        "open_auto_avg_move_pct": round(sum(m.move_pct for m in open_auto) / len(open_auto), 1)
                                  if open_auto else None,
    }


def rule_scorecard(today: date | None = None) -> dict:
    """Per-rule track record of the news engine — the learning loop done
    honestly. Each auto-thesis carries the rule that fired and the engine
    that produced it; grouping resolved theses by rule yields a hit-rate, an
    average realized move, and LIFT over the base rate (does this rule beat
    just-always-betting-a-squeeze?). The model never changes from this — but
    a rule that keeps missing can be pruned, and a future LLM engine is
    scored against the keyword rules on the same ledger (the `source`
    rollup), so contextualization has to EARN its place, not be trusted."""
    auto = load_auto()
    marked = {t["id"]: mark_auto(t, today) for t in auto}
    resolved_all = [m for m in marked.values() if m.status in ("hit", "miss")]
    base_rate = (sum(1 for m in resolved_all if m.status == "hit") / len(resolved_all)
                 if resolved_all else None)

    def group(key_fn) -> list[dict]:
        buckets: dict[str, list[dict]] = {}
        for t in auto:
            buckets.setdefault(key_fn(t), []).append(t)
        rows = []
        for key, items in buckets.items():
            ms = [marked[t["id"]] for t in items]
            res = [m for m in ms if m.status in ("hit", "miss")]
            hits = sum(1 for m in res if m.status == "hit")
            moves = [m.move_pct for m in ms if m.move_pct is not None]
            hit_rate = round(hits / len(res), 3) if res else None
            rows.append({
                "key": key, "n": len(items), "resolved": len(res),
                "hits": hits, "open": sum(1 for m in ms if m.status == "open"),
                "hit_rate": hit_rate,
                "lift": (round(hit_rate - base_rate, 3)
                         if hit_rate is not None and base_rate is not None else None),
                "avg_move_pct": round(sum(moves) / len(moves), 1) if moves else None,
            })
        return sorted(rows, key=lambda r: (-r["resolved"], -r["n"]))

    return {
        "base_rate": round(base_rate, 3) if base_rate is not None else None,
        "n_resolved_total": len(resolved_all),
        "by_rule": group(lambda t: t.get("rule_id", "unattributed")),
        "by_source": group(lambda t: t.get("source", "keyword-rule")),
    }


def render_rule_scorecard(sc: dict) -> str:
    lines = ["NEWS RULE SCORECARD — does each rule's flag actually predict?",
             ""]
    if not sc["by_rule"]:
        return "\n".join(lines + ["_no auto-theses yet — `opencopper news` mints them_"])
    lines.append(f"{'rule':<24}{'n':>4}{'resolved':>9}{'hit rate':>10}{'lift':>8}{'avg move':>10}")
    lines.append("-" * 65)
    for r in sc["by_rule"]:
        hr = f"{r['hit_rate']:.0%}" if r["hit_rate"] is not None else f"({r['open']} open)"
        lift = f"{r['lift']:+.0%}" if r["lift"] is not None else "—"
        mv = f"{r['avg_move_pct']:+.1f}%" if r["avg_move_pct"] is not None else "—"
        lines.append(f"{r['key'][:23]:<24}{r['n']:>4}{r['resolved']:>9}{hr:>10}{lift:>8}{mv:>10}")
    base = f"{sc['base_rate']:.0%}" if sc["base_rate"] is not None else "n/a"
    lines += ["",
              f"base rate (all resolved auto-theses): {base} over {sc['n_resolved_total']}; "
              "lift = rule hit-rate minus base rate.",
              "",
              "engine rollup (the head-to-head a future LLM engine would join):"]
    for r in sc["by_source"]:
        hr = f"{r['hit_rate']:.0%}" if r["hit_rate"] is not None else f"{r['open']} open"
        lines.append(f"  {r['key']:<16} n={r['n']:<4} resolved={r['resolved']:<3} hit-rate {hr}")
    lines += ["",
              "The model NEVER changes from this — it scores the news engine's calls so",
              "dead rules can be pruned and better contextualization has to out-predict",
              "the keyword baseline, not just sound smarter. Accumulates as theses resolve."]
    return "\n".join(lines)


def render_theses(marked: list[Marked]) -> str:
    from .signals import DISCLAIMER

    a = analytics(marked)
    lines = ["THESIS SCORECARD — every call, marked to market",
             f"{'id':<30}{'made':>11}{'deadline':>11}{'status':>10}  reading",
             "-" * 96]
    order = {"hit": 0, "miss": 1, "needs-res": 2, "open": 3}
    for m in sorted(marked, key=lambda m: (order[m.status], m.created)):
        lines.append(f"{m.id[:29]:<30}{m.created:>11}{m.deadline:>11}"
                     f"{m.status.upper():>10}  {m.reading}")
    lines += ["-" * 96,
              f"{a['n']} theses: {a['hit']} hit / {a['miss']} miss / {a['open']} open"
              f" / {a['needs_res']} need resolution"
              + (f" · hit rate {a['hit_rate']:.0%}" if a['hit_rate'] is not None else "")
              + (f" · Brier {a['brier']}" if a['brier'] is not None else "")
              + (f" · open auto theses avg move {a['open_auto_avg_move_pct']:+.1f}%"
                 if a['open_auto_avg_move_pct'] is not None else ""),
              "",
              "Registered theses mirror PREDICTIONS.md; auto theses are generated by the",
              "news pipeline (entry snapshotted at creation, first monthly print ≥ threshold",
              "resolves). Misses stay forever. " + DISCLAIMER]
    return "\n".join(lines)


def theses_json(marked: list[Marked]) -> str:
    return json.dumps({"analytics": analytics(marked),
                       "theses": [asdict(m) for m in marked]}, indent=1)


# ------------------------------------------------------- auto-generation


def generate_auto_theses(hits: list[dict], today: str,
                         auto_path: Path = AUTO_PATH,
                         min_move_pct: float = 5.0,
                         horizon_months: int = 6) -> list[dict]:
    """Turn each distinct rule-matched supply event into a trackable thesis.
    One per (commodity, country, severity, month) — corroborating headlines
    don't multiply theses. Commodities without a keyless monthly series are
    skipped (untrackable claims aren't theses, they're prose). Returns the
    newly added entries."""
    existing = load_auto(auto_path)
    seen = {t["id"] for t in existing}
    added = []
    for h in hits:
        if h["severity"] <= 0:
            continue  # supply additions ease markets; no squeeze thesis
        ident = (f"auto-{today[:7]}-{h['commodity']}-"
                 f"{h['country'].lower().replace(' ', '-').replace('(', '').replace(')', '')}")
        if ident in seen:
            continue
        months = _months(h["commodity"])
        if not months:
            continue
        entry_date, entry_price = months[-1]
        added.append({
            "id": ident,
            "created": today,
            "commodity": h["commodity"],
            "country": h["country"],
            "severity": h["severity"],
            "claim": (f"news-implied supply event ({h['country']} {h['commodity']} "
                      f"-{h['severity']:.0%} prior): price prints ≥+{min_move_pct:.0f}% "
                      f"vs entry within {horizon_months}m"),
            "metric": "price_change",
            "min_move_pct": min_move_pct,
            "horizon_months": horizon_months,
            "entry_date": entry_date,
            "entry_price": entry_price,
            "basis": h["headline"][:140],
            # provenance for the per-rule scorecard: which rule fired and which
            # engine produced it (keyword-rule today; an LLM engine would tag
            # "llm" and be scored head-to-head on the same ledger)
            "rule_id": h.get("rule_id", "unattributed"),
            "source": h.get("source", "keyword-rule"),
        })
        seen.add(ident)
    if added:
        auto_path.parent.mkdir(parents=True, exist_ok=True)
        auto_path.write_text(json.dumps(existing + added, indent=1))
    return added
